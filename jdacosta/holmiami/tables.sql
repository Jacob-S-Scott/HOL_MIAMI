use datbaase HOL;

create or replace schema DATA;

CREATE TABLE IF NOT EXISTS  SEC_FILINGS_CHUNKS (
	DOCUMENT_NAME VARCHAR(16777216),
	CHUNK_ID VARCHAR(16777216),
	CHUNK_TEXT VARCHAR(16777216),
	DOCUMENT_DATE DATE
);
CREATE TABLE IF NOT EXISTS  STOCK_NEWS (
	TICKER VARCHAR(10) NOT NULL,
	ID VARCHAR(50) NOT NULL,
	TITLE VARCHAR(1000),
	SUMMARY VARCHAR(16777216),
	DESCRIPTION VARCHAR(16777216),
	PUBLISHER VARCHAR(100),
	LINK VARCHAR(2000),
	PUBLISH_TIME TIMESTAMP_NTZ(9),
	DISPLAY_TIME TIMESTAMP_NTZ(9),
	CONTENT_TYPE VARCHAR(50),
	THUMBNAIL_URL VARCHAR(2000),
	IS_PREMIUM BOOLEAN,
	IS_HOSTED BOOLEAN,
	DOWNLOAD_TIMESTAMP TIMESTAMP_NTZ(9),
	primary key (TICKER, ID)
);
CREATE TABLE IF NOT EXISTS  STOCK_NEWS_STAGING (
	TICKER VARCHAR(16777216),
	ID VARCHAR(16777216),
	TITLE VARCHAR(16777216),
	SUMMARY VARCHAR(16777216),
	DESCRIPTION VARCHAR(16777216),
	PUBLISHER VARCHAR(16777216),
	LINK VARCHAR(16777216),
	PUBLISH_TIME VARCHAR(16777216),
	DISPLAY_TIME VARCHAR(16777216),
	CONTENT_TYPE VARCHAR(16777216),
	THUMBNAIL_URL VARCHAR(16777216),
	IS_PREMIUM BOOLEAN,
	IS_HOSTED BOOLEAN,
	DOWNLOAD_TIMESTAMP VARCHAR(16777216)
);
CREATE TABLE IF NOT EXISTS  STOCK_PRICE_HISTORY (
	DATE DATE NOT NULL,
	OPEN_PRICE FLOAT,
	HIGH_PRICE FLOAT,
	LOW_PRICE FLOAT,
	CLOSE_PRICE FLOAT,
	VOLUME NUMBER(38,0),
	DIVIDENDS FLOAT,
	STOCK_SPLITS FLOAT,
	TICKER VARCHAR(10) NOT NULL,
	DOWNLOAD_TIMESTAMP TIMESTAMP_NTZ(9),
	primary key (TICKER, DATE)
);